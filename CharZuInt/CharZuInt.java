public class CharZuInt {
  public static void main ( String [] args ) {
    
    char buchstabe = 'F';//ASCII Wert -> 70
    int zahl = 13; //Integer 
    int summe = buchstabe + zahl; // summe wird zu buchstabe (70) + zahl (13)
    
    System.out.println(summe);  //Ausgabe des Ergebnisses in einer Zeile
}
}